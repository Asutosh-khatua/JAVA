public class Day3Assignment {
    public static void main(String[] args) {
        int a = 10;           // Simple assignment
        a += 5;               // Now a = 15
        a -= 3;               // Now a = 12
        a *= 2;               // Now a = 24
        a /= 4;               // Now a = 6
        a %= 5;               // Now a = 1

        System.out.println("Final value of a: " + a);
    }
}
