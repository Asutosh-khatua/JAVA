public class Day3Arithmetic {
    public static void main(String[] args) {
        int a = 15;
        int b = 4;

        System.out.println("Addition: " + (a + b));        // 15 + 4 = 19
        System.out.println("Subtraction: " + (a - b));     // 15 - 4 = 11
        System.out.println("Multiplication: " + (a * b));  // 15 * 4 = 60
        System.out.println("Division: " + (a / b));        // 15 / 4 = 3 (integer division)
        System.out.println("Modulus: " + (a % b));         // 15 % 4 = 3
    }
}
