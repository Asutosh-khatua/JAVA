//wap to add all the element present inside given array. 
public class Day5Array5 {
    public static void main(String[] args) {
   
        int[] arr={2,3,4,5,6,7,8};
        int sum=0;
        
        for(int i:arr)
        {
            sum=sum+i;
        }
        System.out.println(sum);
    } 
}
